pub fn run() {
    // Integer
    let p: i8 = 1;
    println!("Integer: {} ", p);

    // Floating-Point
    let x = 5.0; // f64
    println!("Floating-Point: {} ", x);

    // Char
    let a = 'A';
    println!("Char: {} ", a);

    // String
    let y = String::from("Bob");
    println!("String: {} ", y);

    // Boolean
    let b : bool = true;
    println!("Bool: {} ", b);

    // Array
    let a = [1, 2, 3, 4, 5];
    println!("Array: index0 = {} ", a[0]);
}